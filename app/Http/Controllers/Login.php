    {
        $validate = $request->validate([
            'email' => 'required',
            'password' => 'required'
        ]);

        if(Auth::attempt($validate)){

            $request->session()->regenerate();
            return redirect('/dashboard');

        }

        notify()->error('email or password is wrong !', 'Failed');
        return back();
    }

